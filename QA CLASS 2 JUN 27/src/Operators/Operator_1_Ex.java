package Operators;

public class Operator_1_Ex {

	public static void main(String[] args) {
		
		int a=80;
		
		int b=35;
		
		int c=80;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);// a>b-T or a=b-F
		System.out.println(a<=b);//a<b-F or a=b -F
		
		System.out.println(a>=c);
		
		System.out.println(a<=c);
		
		
	}

}
