package Loops;

public class Switch_ex {

	public static void main(String[] args) {
		
		char grade='C';
		
		
		switch(grade) 
		{
		case 'A':
		System.out.println(" FIRST CLASS");
		break;
		
		case 'B':
		System.out.println("SECOND CLASS");
		break;
		case 'C':
		System.out.println("THIRD CLASS");
		break;
		default:
		System.out.println("invalid code");
		
		
		}
		

	}

}
